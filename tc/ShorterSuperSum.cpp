using namespace std;
int dp[16][16]={0};
class ShorterSuperSum {
public:
	int calculate(int, int);
};



int ShorterSuperSum::calculate(int k, int n) {
	cout<<"hr ka op"<<endl;
	if(k==0 )return n;
	if(n==0 )return 0;
	if(dp[k][n]!=0)return dp[k][n];
	else {
		
		dp[k][n]= ShorterSuperSum::calculate(k,n-1) + calculate(k-1,n); 
	}
	return dp[k][n];
}




