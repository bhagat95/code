class ColorfulRoad {
public:
	int getMin(string);
};

int ColorfulRoad::getMin(string road) {
	int len = road.length();
	map<char,char>next = { {'R','G'}, {'G','B'}, {'B','R'} };
	int cost[len];
	fill(cost,cost+len,INT_MAX);
	cost[len-1]=0;
	for(int i=len-2;i>=0;i--){
		for(int j=i+1;j<len;j++){
			if(next[road[i]]==road[j]){
				cost[i]=min(cost[i],((j-i)*(j-i))+cost[j]);
			}
		}
	}
	if(cost[0]>=INT_MAX)return -1;
	else return cost[0];
}


